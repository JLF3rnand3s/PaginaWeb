package model;

public class Pista {
	
	//atributos
	public int codigo, numero;
	
	
	//constructores
	public Pista(int codigo, int numero) {
		super();
		this.codigo = codigo;
		this.numero = numero;
	}

	
	//getters y setters
	public int getCodigo() {
		return codigo;
	}


	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}


	public int getNumero() {
		return numero;
	}


	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
	
	
	
	
	

}
